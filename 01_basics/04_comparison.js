// In this the number which is written as a string is converted into a number and then it is compared and gives a output.

console.log("2">1);
console.log("02">1);

// Here "==" in this operator treated differently as compare to other operators like <,>,<=,>=.
// The "Null" is  converted into "0"

console.log(null>0);
console.log(null==0);
console.log(null>=0);

// Here in this "===" no conversion of datatype is done. It strictly checks the datatype in its original form.

console.log("2"===2);
